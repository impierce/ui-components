<script lang="ts">
  import { createCombobox } from '@melt-ui/svelte';
  import { onMount } from 'svelte';
  // import Check from '~icons/lucide/check?raw&width=16&height=16';
  // import ChevronDown from '~icons/lucide/chevron-down?raw&width=16&height=16';
  // import ChevronUp from '~icons/lucide/chevron-up';
  import { Check, ChevronDown, ChevronUp } from 'svelte-heros-v2';

  interface Book {
    author: string;
    title: string;
    disabled: boolean;
  }

  let books: Book[] = [
    {
      author: 'Harper Lee',
      title: 'To Kill a Mockingbird',
      disabled: false
    },
    {
      author: 'Lev Tolstoy',
      title: 'War and Peace',
      disabled: false
    },
    {
      author: 'Fyodor Dostoyevsy',
      title: 'The Idiot',
      disabled: true
    },
    {
      author: 'Oscar Wilde',
      title: 'A Picture of Dorian Gray',
      disabled: false
    },
    {
      author: 'George Orwell',
      title: '1984',
      disabled: false
    },
    {
      author: 'Jane Austen',
      title: 'Pride and Prejudice',
      disabled: false
    },
    {
      author: 'Marcus Aurelius',
      title: 'Meditations',
      disabled: false
    },
    {
      author: 'Fyodor Dostoevsky',
      title: 'The Brothers Karamazov',
      disabled: false
    },
    {
      author: 'Lev Tolstoy',
      title: 'Anna Karenina',
      disabled: false
    },
    {
      author: 'Fyodor Dostoevsky',
      title: 'Crime and Punishment',
      disabled: false
    }
  ];

  onMount(() => console.log(typeof ChevronDown));

  const { open, input, menu, item, inputValue, isSelected, filteredItems } = createCombobox({
    filterFunction: (item, inputValue) => {
      // Example string normalization function. Replace as needed.
      const normalize = (str: string) => str.normalize().toLowerCase();
      const normalizedInput = normalize(inputValue);
      return (
        normalizedInput === '' ||
        normalize(item.title).includes(normalizedInput) ||
        normalize(item.author).includes(normalizedInput)
      );
    },
    items: books,
    itemToString: (item) => item.title
  });
</script>

<div class="max-w-max">
  <label class="cursor-pointer">
    <span class="block pb-1 capitalize">Choose your favorite book:</span>
    <div class="relative">
      <input
        {...$input}
        class="flex h-10 items-center justify-between rounded-md
              bg-slate-100 px-3 pr-12 text-blue-700"
        placeholder="Best book ever"
        use:input
        value={$inputValue}
      />
      <div class="absolute right-2 top-1/2 z-10 -translate-y-1/2 text-blue-700">
        {#if $open}
          <ChevronUp />
        {:else}
          <ChevronDown />
        {/if}
      </div>
    </div>
  </label>

  <div class="z-10 flex max-h-[300px] flex-col overflow-hidden rounded-md" {...$menu} use:menu>
    <ul class="flex max-h-full flex-col gap-2 overflow-y-auto bg-slate-100 px-2 py-2">
      {#if $open}
        {#if $filteredItems.length !== 0}
          {#each $filteredItems as book, index (index)}
            <li
              {...$item({
                index,
                item: book,
                disabled: book.disabled
              })}
              use:item
              class="relative cursor-pointer rounded-md py-1 pl-9 pr-4 text-neutral-800
                          data-[highlighted]:bg-blue-100 data-[highlighted]:text-blue-700
                          data-[disabled]:opacity-50"
            >
              {#if $isSelected(book)}
                <div class="check">
                  <Check />
                </div>
              {/if}
              <div>
                <span>{book.title}</span>
                <span class="block text-sm opacity-70">{book.author}</span>
              </div>
            </li>
          {/each}
        {:else}
          <li
            class="relative cursor-pointer rounded-md py-1 pl-8 pr-4
                      text-neutral-800 data-[highlighted]:bg-blue-100
                      data-[highlighted]:text-blue-700"
          >
            No results found
          </li>
        {/if}
      {/if}
    </ul>
  </div>
</div>

